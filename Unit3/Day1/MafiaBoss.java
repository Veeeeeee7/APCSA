package Unit3.Day1;

public class MafiaBoss {
    public String name;
    public boolean arrested;
    public String territory;
    public int henchmenTotal;

    public MafiaBoss(String name, boolean arrested, String territory, int henchmenTotal) {
        this.name = name;
        this.arrested = arrested;
        this.territory = territory;
        this.henchmenTotal = henchmenTotal;
    }
}
