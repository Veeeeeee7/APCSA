package Unit1Day1;

public class EscapeTester {
    public static void main(String[] args) {
        double d1 = 0;
        for (int i = 1; i<=8; i++) {
            d1 += 0.1; // += ADDS the value on the right and assigns the value to the variable
            // another way of writing d1 = d1 + 0.1;

            // d1 = 0.8
        }
        
        double d2 = 0.1*8;

        System.out.println("d1 = " + d1);
        System.out.println("d2 = " + d2);

        // order of operations
        /*
         * Parenthesis () or Brackets [] or Member access .getX() -> Exponents -> Multiply or Divide -> Addition or Subtraction -> Relational Comparisons ==, <=, >=
         * 
         */
        
        
    }
}
